//API que gera número de 0 49
let numeroJaSorteados = [];
let numeroAtual = -1;
let nPerguntas = [];

function getRandomIntInclusive() {
    let min = Math.ceil(0);
    let max = Math.floor(40);
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function sortearPergunta() {
    if (numeroAtual = -1){
        numeroAtual = getRandomIntInclusive();
        numeroJaSorteados.push(numeroAtual);
        nPerguntas.push(numeroAtual);
        return;
    }
    numeroAtual = getRandomIntInclusive();
    verificarRepetição(numeroAtual);
}

function verificarRepetição(nmr_A)
{
    for (let i = 0; i < numeroJaSorteados.length; i++) {
        let cont = 0;
        if (nmr_A == numeroJaSorteados[i]) {
            nmr_A == getRandomIntInclusive();
            verificarRepetição();
        }
        cont++;
    }
    if (cont == numeroJaSorteados.length){
        nPerguntas.push(nmr_A);
        numeroJaSorteados.push(nmr_A);
    }
}

